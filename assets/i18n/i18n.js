// HTML lang attribute values per language code
const LANG_ATTR = { es: 'es', va: 'ca', it: 'it', en: 'en' };

// ─── KEY RESOLVER ────────────────────────────────────────────────────────────
// Resolves a dot-separated key path (e.g. "slide01.text.0") by first looking
// in translations[lang], then falling back to uiStrings[lang].
// Numeric path segments work as array indices: array["0"] === array[0] in JS.
function resolveKey(lang, keyPath) {
  const keys = keyPath.split('.');

  if (typeof translations !== 'undefined' && translations[lang]) {
    let val = translations[lang];
    for (const k of keys) {
      if (val == null) { val = undefined; break; }
      val = val[k];
    }
    if (val !== undefined) return val;
  }

  let val = uiStrings[lang];
  for (const k of keys) {
    if (val == null) return undefined;
    val = val[k];
  }
  return val;
}

// ─── SET LANGUAGE ────────────────────────────────────────────────────────────
function setLanguage(lang) {
  if (!['es', 'va', 'it', 'en'].includes(lang)) lang = 'es';

  document.documentElement.lang = LANG_ATTR[lang];
  document.documentElement.setAttribute('data-lang', lang);
  localStorage.setItem('lh-lang', lang);

  // Update desktop pill buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Update mobile compact selector
  const mobileLabel = document.getElementById('lang-mobile-current');
  if (mobileLabel) mobileLabel.textContent = lang.toUpperCase();
  document.querySelectorAll('.lang-mobile-option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });

  // Translate all marked elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const value = resolveKey(lang, key);

    // Skip if not found or resolved to a plain object (container key)
    if (value === undefined || value === null) return;
    if (typeof value === 'object' && !Array.isArray(value)) return;

    if (Array.isArray(value)) {
      // UL/OL → render <li> items; anything else → render <p> items
      if (el.tagName === 'UL' || el.tagName === 'OL') {
        el.innerHTML = value.map(item => `<li>${item}</li>`).join('');
      } else {
        el.innerHTML = value.map(item => `<p>${item}</p>`).join('');
      }
    } else {
      el.textContent = String(value);
    }
  });

  // ── Slide 04: re-render open step detail in new language ─────────────────
  const activeStepBtn = document.querySelector('.tl-step.active');
  if (activeStepBtn) {
    const allStepBtns = Array.from(document.querySelectorAll('.tl-step'));
    const idx = allStepBtns.indexOf(activeStepBtn);
    const steps = translations[lang] && translations[lang].slide04 && translations[lang].slide04.steps;
    if (idx >= 0 && steps && steps[idx]) {
      const step = steps[idx];
      const sdTitle = document.getElementById('sd-title');
      const sdDesc  = document.getElementById('sd-desc');
      const sdNum   = document.getElementById('sd-num');
      if (sdTitle) sdTitle.textContent = step.title;
      if (sdDesc)  sdDesc.innerHTML = step.bullets.map(b => `<li>${b}</li>`).join('');
      if (sdNum) {
        const prefix = uiStrings[lang] && uiStrings[lang].s4 ? uiStrings[lang].s4.stepPrefix : 'Paso';
        sdNum.textContent = `${prefix} ${idx + 1}`;
      }
    }
  }
}

// ─── MOBILE DROPDOWN ─────────────────────────────────────────────────────────

function closeLangDropdown() {
  const menu   = document.getElementById('lang-mobile-menu');
  const toggle = document.getElementById('lang-mobile-toggle');
  if (menu)   menu.classList.remove('open');
  if (toggle) toggle.setAttribute('aria-expanded', 'false');
}

function toggleLangDropdown() {
  const menu   = document.getElementById('lang-mobile-menu');
  const toggle = document.getElementById('lang-mobile-toggle');
  if (!menu || !toggle) return;
  const opening = !menu.classList.contains('open');
  menu.classList.toggle('open', opening);
  toggle.setAttribute('aria-expanded', String(opening));
}

function selectMobileLang(lang) {
  setLanguage(lang);
  closeLangDropdown();
}

// ─── INIT ────────────────────────────────────────────────────────────────────
document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('lh-lang') || 'es';
  setLanguage(saved);

  // Close mobile dropdown when clicking outside
  document.addEventListener('click', e => {
    if (!e.target.closest('.lang-mobile')) closeLangDropdown();
  });
});
